# Every line starting with # is a comment.
# This is a comment.
# Comments are used to explain the code.
# Comments are ignored when running the code.

"""
This is a multiline comment using triple double quotes.
It spans multiple lines.
"""

''' 
This is also a multiline comment using triple single quotes.
It can span multiple lines as well.
'''

# Triple quotes are typically used for docstrings or multiline strings, not for inline comments.
# Only the # symbol can be used for comments after a command.

print("Hello World!")  # This is an example of a comment after a command

# You can comment one or multiple lines of code to exclude them from the execution of the program
print("Hello, ")
# print("I am ")  # This line of code is commented out and will not be executed at runtime.
