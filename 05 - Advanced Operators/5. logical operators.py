# //and, &
# -True : if both are True
# 1 and 1 = True
# 1 and 0 = False
# 0 & 1 = False
# 0 & 0 = t

# //or, |
# -True : if one or both are True
# 1 or 1 = True
# 1 or 0 = False
# 0 | 1 = True
# 0 | 0 = False

# //not, ~
# -True : if False; False : if True
# not 1 = False
# ~ 0 = True

# //xor
# -True : if one is True and the other is False
# 1 ^ 1 = False
# 1 ^ 0 = True
# 0 ^ 1 = True
# 0 ^ 0 = False
