
# The indented part of any control flow statement is called the code block or body.
# Indentation in Python indicates which statements are included in the control structure.
# It is a way to show which lines of code are "inside" a loop, condition, or function.
# The typical indentation used in Python is 4 spaces.

if True:
    print("This is inside the if block")  # This is part of the 'if' code block
    for i in range(3):
        print(i)  # This is part of the 'for' code block
print("This is outside the if and for blocks")  # This is not indented; it's outside the control structures
